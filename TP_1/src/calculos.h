#ifndef CALCULOS_H_
#define CALCULOS_H_

/**
 * \brief Realiza la suma de dos operandos ingresados por el usario
 *
 * \pre
 * \post
 * \param x Corresponde al primer operando ingresado por el usuario
 * \param y Corresponde al segundo operando ingresado por el usuario
 * \return Devuelve el resultado de la suma float de los dos operando
 */
float suma(float x, float y);

/**
 * \brief Realiza la resta de dos operandos ingresados por el usario
 *
 * \pre
 * \post
 * \param x Corresponde al primer operando ingresado por el usuario
 * \param y Corresponde al segundo operando ingresado por el usuario
 * \return Devuelve el resultado de la resta float de los dos operando
 */
float resta(float x, float y);

/**
 * \brief Realiza la división de dos operandos ingresados por el usario
 *
 * \pre
 * \post
 * \param x Corresponde al primer operando ingresado por el usuario
 * \param y Corresponde al segundo operando ingresado por el usuario
 * \return Devuelve el resultado de la división float de los dos operando
 */
float division(float x, float y);

/**
 * \brief Realiza la multiplicación de dos operandos ingresados por el usario
 *
 * \pre
 * \post
 * \param x Corresponde al primer operando ingresado por el usuario
 * \param y Corresponde al segundo operando ingresado por el usuario
 * \return Devuelve el resultado de la multiplicación float de los dos operando
 */
float multiplicacion(float x, float y);

/**
 * \brief Realiza el factorial del primer operando ingresado por el usuario
 *
 * \pre
 * \post
 * \param x Corresponde al primer operando ingresado por el usuario
 * \return Devuelve el resultado del factorial del primer operando
 */
float factorialPrimerOperando(float x);

/**
 * \brief Realiza el factorial del segundo operando ingresado por el usuario
 *
 * \pre
 * \post
 * \param y Corresponde al segundo operando ingresado por el usuario
 * \return Devuelve el resultado del factorial del segundo operando
 */
float factorialSegundoOperando(float y);


#endif /* CALCULOS_H_ */
